#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	s_len;
	char			*str;
	unsigned int	i;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	i = 0;
	str = (char *)ft_calloc(s_len + 1, sizeof(char));
	if (str == NULL)
		return (NULL);
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	return (str);
}
